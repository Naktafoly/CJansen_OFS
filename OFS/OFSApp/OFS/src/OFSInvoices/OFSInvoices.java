    private void FillCbNamen() {
        List<Addressbook> addressbook = AdressbookServices.GetAll();
        DefaultComboBoxModel model = new DefaultComboBoxModel();
        model.addElement("-- Select een Naam --");
        for(Addressbook obj:addressbook){
            model.addElement(obj);
        }
        cbNamen.setModel(model);
    }
    
    private void FillCbBetaald() {
        List<Addressbook> addressbook = AdressbookServices.GetAll();
        DefaultComboBoxModel model = new DefaultComboBoxModel();
        model.addElement("-- Select een Naam --");
        for(Addressbook obj:addressbook){
            model.addElement(obj);
        }
        cbNamen.setModel(model);
    }

    private void Save(Invoices invoice) {
        if(cbNamen.getSelectedIndex()>0){
            if("".equals(txtBedrag.getText()) && "".equals(txtBetalen.getText()) 
                    && "".equals(txtDate.getText()) && "".equals(txtFormulier.getText())){
               InvoicesServices.Save(invoice);
            }
        }
    }

    private void Clear() {        
        DefaultListModel model = new DefaultListModel();
        lstFacturen.setModel(model);
        txtBedrag.setText("");
        txtBetalen.setText("");
        txtDate.setText("");
        txtFormulier.setText("");
        cbNamen.setSelectedIndex(0);
    }
