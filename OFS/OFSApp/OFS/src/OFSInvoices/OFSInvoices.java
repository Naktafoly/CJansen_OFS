    private void btnZoekenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnZoekenActionPerformed
        if(cbNamen.getSelectedIndex()>0){
            Addressbook addressbook = (Addressbook)cbNamen.getSelectedItem();
            DefaultListModel model = new DefaultListModel();
            List<Invoices> list = InvoicesServices.GetAll();
            List<Invoices> invoices = null;
            for(Invoices i:list){
                if(i.getNaam() == addressbook){
                   invoices.add(i); 
                }                
            }
            for(Invoices obj:invoices){
                model.addElement(obj);
            }
            lstFacturen.setModel(model);
            
        }
    }//GEN-LAST:event_btnZoekenActionPerformed

