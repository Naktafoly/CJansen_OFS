    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        DateFormat format = new SimpleDateFormat("dd-MM-yyyy");
        Invoices invoice = new Invoices();
        Addressbook addressbook = (Addressbook)cbNamen.getSelectedItem();
        invoice.setNaam(addressbook);
        if("Ja".equals(cbBetaald.getSelectedItem().toString())){
            invoice.setBetaald(true);
        }
        else invoice.setBetaald(false);
        invoice.setPrijs(Double.parseDouble(txtBedrag.getText()));
        try {
            Date date = new Date();
            date = format.parse(txtBetalen.getText());
            invoice.setVervaldatum(date);
        } catch (ParseException ex) {
            Logger.getLogger(OFSInvoices.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            Date date = new Date();
            date = format.parse(txtDate.getText());
            invoice.setOntvangen(date);
        } catch (ParseException ex) {
            Logger.getLogger(OFSInvoices.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        //Nog op te lossen
        //invoice.setFormulier(Byte.parseByte(txtFormulier.getText()));
        
        Save(invoice);
        Clear();
    }//GEN-LAST:event_btnSaveActionPerformed

